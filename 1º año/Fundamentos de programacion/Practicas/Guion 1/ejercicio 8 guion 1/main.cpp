#include <iostream>

using namespace std;

int main()
{
    int n1, n2;
    cout << "Introduce un numero entero: ";
    cin >>n1;
    cout << "Introduce otro numero entero: ";
    cin >>n2;
    cout << "La suma de esos dos numeros es: " << n1 + n2 << endl;
    cout << "La diferencia entre esos dos numeros es: " << n1 - n2 << endl;
    cout << "El producto de esos numeros es: " << n1 * n2 << endl;
    cout << "El cociente de esos dos numeros es: " << n1/n2 << endl;
    return 0;
}
